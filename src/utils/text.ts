/**
 * utils/text.ts
 * --------------------------
 * Утилиты для работы с текстом и Markdown.
 * Централизованное экранирование для безопасности и единообразия.
 */

/**
 * Экранирование спецсимволов для MarkdownV2.
 * 
 * MarkdownV2 требует экранирования следующих символов:
 * _ * [ ] ( ) ~ ` > # + - = | { } . !
 * 
 * Примечание: для URL в ссылках MarkdownV2 обычно не требует экранирования точек,
 * но для текста ссылки и обычного текста нужно экранировать все спецсимволы.
 * 
 * @param text - текст для экранирования
 * @returns экранированный текст
 * 
 * @example
 * escapeMdV2("Привет (мир)!") // "Привет \\(мир\\)\\!"
 */
export function escapeMdV2(text: string): string {
	if (!text) return "";
	// Экранируем все спецсимволы MarkdownV2: _ * [ ] ( ) ~ ` > # + - = | { } . !
	return String(text).replace(/([_*\[\]()~`>#+\-=|{}.!])/g, "\\$1");
}

/**
 * Экранирование URL для использования в ссылках MarkdownV2.
 * 
 * В MarkdownV2 для URL в ссылках нужно экранировать только некоторые символы:
 * ) и \
 * 
 * @param url - URL для экранирования
 * @returns экранированный URL
 * 
 * @example
 * escapeUrlV2("https://example.com/path)") // "https://example.com/path\\)"
 */
export function escapeUrlV2(url: string): string {
	if (!url) return "";
	// В URL экранируем только ) и \
	// Используем класс символов для более эффективного поиска
	return String(url).replace(/[)\\]/g, "\\$&");
}

